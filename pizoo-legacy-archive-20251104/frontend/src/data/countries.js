export const getAllCountries = () => {
  // Remove duplicates from ALL_COUNTRIES that are in POPULAR_COUNTRIES
  const popularCodes = POPULAR_COUNTRIES.map(c => c.code);
  const remainingCountries = ALL_COUNTRIES.filter(c => !popularCodes.includes(c.code));
  
  return [...POPULAR_COUNTRIES, ...remainingCountries];
};
